package io.stargate.api.sql.server.postgres.msg;

public class NoticeResponse extends MessageResponse {

  private NoticeResponse(String severity, String message) {
    super(NOTICE, severity, message);
  }

  public static NoticeResponse warning(String message) {
    return new NoticeResponse("WARNING", message);
  }
}
